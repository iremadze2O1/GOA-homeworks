function sort(initialArray, sortingArray) {
    let sortedArray = [];
    for (let i = 0; i < sortingArray.length; i++) {
      sortedArray.push(initialArray[sortingArray[i]]);
    }
    return sortedArray;
}

function sort(initialArray, sortingArray) {
  let sortedArray = [];
  for (let i = 0; i < sortingArray.length; i++) {
    sortedArray[sortingArray[i]] = initialArray[i];
  }
  return sortedArray;
}


